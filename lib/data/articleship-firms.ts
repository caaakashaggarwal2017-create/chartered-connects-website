export function getSimilarFirms(firm: ArticleshipFirm, count = 3): ArticleshipFirm[] {
  return articleshipFirms
    .filter(f => f.id !== firm.id && (f.city === firm.city || f.specialisations.some(s => firm.specialisations.includes(s))))
    .slice(0, count)
}
